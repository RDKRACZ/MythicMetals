package nourl.mythicmetals.armor;

import net.minecraft.client.model.ModelPart;
import net.minecraft.client.render.VertexConsumer;
import net.minecraft.client.util.math.MatrixStack;
import net.minecraft.entity.EquipmentSlot;

public class MetallurgiumArmorModel extends MythicArmorModel {
    private final ModelPart HEAD;

    public MetallurgiumArmorModel(EquipmentSlot slot) {
        super(slot);

        textureWidth = 64;
        textureHeight = 32;

        HEAD = new ModelPart(this);
        setRotationAngle(HEAD,0.0F, 0.0F, 0.0F);
        HEAD.setTextureOffset(0, 0).addCuboid(-4.0F, -8.0F, -4.0F, 8.0F, 8.0F, 8.0F, 1.0F, false);
        HEAD.setTextureOffset(32, 6).addCuboid(4.9F, -10.0F, -4.9F, 1.0F, 3.0F, 2.0F, 0.0F, false);
        HEAD.setTextureOffset(32, 0).addCuboid(-5.9F, -10.0F, -4.9F, 1.0F, 3.0F, 2.0F, 0.0F, false);
        HEAD.setTextureOffset(32, 0).addCuboid(-5.9F, -12.0F, -4.9F, 1.0F, 2.0F, 1.0F, 0.0F, false);
        HEAD.setTextureOffset(32, 6).addCuboid(4.9F, -12.0F, -4.9F, 1.0F, 2.0F, 1.0F, 0.0F, false);
    }

    @Override
    public void render(MatrixStack matrixStack, VertexConsumer buffer, int packedLight, int packedOverlay, float red, float green, float blue, float alpha){        HEAD.render(matrixStack, buffer, packedLight, packedOverlay);
        head = HEAD;
        head.visible = slot == EquipmentSlot.HEAD;
        HEAD.render(matrixStack, buffer, packedLight, packedOverlay);

    }
}
